// タイルに関する型定義

// ゲームマップで使用するタイルの種類（初学者向け：地形の種類を定義）
export type タイルタイプ = 
  | 'grass'    // 草地（通行可能）
  | 'tree'     // 木（通行不可）
  | 'stone'    // 石（通行不可）
  | 'water';   // 水（通行不可）

// タイルの詳細情報（初学者向け：各タイルの性質を定義）
export interface タイル情報 {
  タイプ: タイルタイプ;
  通行可能: boolean;
  表示名: string;
}

